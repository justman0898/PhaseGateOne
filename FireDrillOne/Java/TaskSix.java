public class TaskSix{
	
	public static void main(String[] args){		
		int counter = 0;		
		int temp = 1;
		while(true){
			temp = temp * 4;
			System.out.print(temp+" ");
			counter++;
		
			if(counter == 5) break; 
		}
		temp = 1;
		counter = 0;				
		while(true){
			temp = temp * 8;
			System.out.print(temp+" ");
			counter++;
		
			if(counter == 5) break; 
		}		


	}
}
